function changelivecity(){
    var index = $("#livesf")[0].selectedIndex-1;
    if (index>=0) {
        $("#livecity").empty();
        for(var i = 0;i<city[index].length;i++){
            var text = city[index][i].split("-");
            $("<option value='"+text[1]+"'>"+text[0]+"</option>").appendTo($("#livecity")[0]);
        }
    }
}

